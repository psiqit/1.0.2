# Helper Functions
# ============================================================

def _qr_decomposition(A):
    """
    QR decomposition using Gram-Schmidt process.

    Args:
        A: Input matrix.

    Returns:
        Tuple (Q, R) where Q is unitary and R is upper triangular.
    """
    n = len(A)
    Q = []
    
    # Transpose for column vectors
    A_cols = [[A[i][j] for i in range(n)] for j in range(n)]
    
    for j in range(n):
        v = A_cols[j][:]
        for k in range(j):
            # Projection
            dot = sum(Q[k][i].conjugate() * v[i] for i in range(n))
            for i in range(n):
                v[i] -= dot * Q[k][i]
        
        # Normalize
        norm_v = math.sqrt(sum(abs(x)**2 for x in v))
        if norm_v > 1e-12:
            v = [x / norm_v for x in v]
        else:
            v = [0.0] * n
        
        Q.append(v)
    
    # Transpose Q to get matrix
    Q_mat = [[Q[j][i] for j in range(n)] for i in range(n)]
    
    # Compute R = Q† A
    R = [[0j] * n for _ in range(n)]
    for i in range(n):
        for j in range(n):
            for k in range(n):
                R[i][j] += Q[i][k].conjugate() * A[k][j]
    
    return Q_mat, R


def set_random_seed(seed: int):
    """
    Set the global random seed for reproducibility.

    Args:
        seed: Random seed.

    Examples:
        >>> set_random_seed(42)
        >>> psi1 = random_state(2)
        >>> set_random_seed(42)
        >>> psi2 = random_state(2)
        >>> assert psi1.data == psi2.data
    """
    random.seed(seed)


