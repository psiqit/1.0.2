def random_unitary(dim: int, seed: Optional[int] = None) -> List[List[complex]]:
    """
    Generate a random unitary matrix according to the Haar measure.

    The algorithm generates a random complex matrix with Gaussian entries,
    performs QR decomposition, and adjusts the diagonal of R to be positive.

    Args:
        dim: Dimension of the unitary matrix.
        seed: Random seed for reproducibility.

    Returns:
        Random unitary matrix.

    Examples:
        >>> U = random_unitary(2)
        >>> from psiqit.utils.validation import is_unitary
        >>> print(is_unitary(U).is_valid)
        True
        >>> 
        >>> # 4x4 unitary (2-qubit gate)
        >>> U = random_unitary(4)
    """
    if seed is not None:
        random.seed(seed)
    
    # Generate random complex matrix
    Z = []
    for _ in range(dim):
        row = []
        for _ in range(dim):
            real = random.gauss(0, 1)
            imag = random.gauss(0, 1)
            row.append(complex(real, imag))
        Z.append(row)
    
    # QR decomposition
    Q, R = _qr_decomposition(Z)
    
    # Make diagonal of R positive
    for i in range(dim):
        if R[i][i].real < 0:
            for j in range(dim):
                Q[j][i] *= -1
    
    return Q


def random_hermitian(dim: int, seed: Optional[int] = None) -> List[List[complex]]:
    """
    Generate a random Hermitian matrix.

    The matrix is generated as H = (A + A†)/2, where A is a random
    complex matrix with Gaussian entries.

    Args:
        dim: Dimension of the Hermitian matrix.
        seed: Random seed for reproducibility.

    Returns:
        Random Hermitian matrix.

    Examples:
        >>> H = random_hermitian(2)
        >>> from psiqit.utils.validation import is_hermitian
        >>> print(is_hermitian(H).is_valid)
        True
        >>> 
        >>> # Random Hamiltonian for a 3-level system
        >>> H = random_hermitian(3)
    """
    if seed is not None:
        random.seed(seed)
    
    # Generate random matrix
    A = []
    for _ in range(dim):
        row = []
        for _ in range(dim):
            real = random.gauss(0, 1)
            imag = random.gauss(0, 1)
            row.append(complex(real, imag))
        A.append(row)
    
    # Make Hermitian: H = (A + A†)/2
    A_dag = dagger(A)
    H = [[(A[i][j] + A_dag[i][j]) / 2 for j in range(dim)] for i in range(dim)]
    
    return H


def random_positive_operator(dim: int, seed: Optional[int] = None) -> List[List[complex]]:
    """
    Generate a random positive semidefinite operator.

    The operator is generated as P = H², where H is a random Hermitian matrix.
    This guarantees that P is positive semidefinite.

    Args:
        dim: Dimension of the operator.
        seed: Random seed for reproducibility.

    Returns:
        Random positive semidefinite matrix.

    Examples:
        >>> P = random_positive_operator(2)
        >>> from psiqit.utils.validation import is_positive_semidefinite
        >>> print(is_positive_semidefinite(P).is_valid)
        True
    """
    if seed is not None:
        random.seed(seed)
    
    # Generate random Hermitian
    H = random_hermitian(dim, seed)
    
    # Square to make positive
    P = mul(H, H)
    
    return P


def random_pauli_rotation(seed: Optional[int] = None) -> Tuple[str, float]:
    """
    Generate a random Pauli rotation.

    Returns:
        Tuple of (pauli_gate, angle) where pauli_gate is 'X', 'Y', or 'Z'.

    Examples:
        >>> gate, angle = random_pauli_rotation()
        >>> print(f"Apply R{gate}({angle:.3f})")
        Apply RX(2.718)
        >>> 
        >>> # For reproducibility
        >>> gate, angle = random_pauli_rotation(seed=42)
    """
    if seed is not None:
        random.seed(seed)
    
    gates = ['X', 'Y', 'Z']
    gate = random.choice(gates)
    angle = random.uniform(0, 2 * math.pi)
    
    return gate, angle


def random_pauli_string(n_qubits: int, seed: Optional[int] = None) -> str:
    """
    Generate a random Pauli string (tensor product of Pauli operators).

    Args:
        n_qubits: Number of qubits.
        seed: Random seed for reproducibility.

    Returns:
        String like 'XIZY' where each character is I, X, Y, or Z.

    Examples:
        >>> pauli_string = random_pauli_string(3)
        >>> print(pauli_string)  # e.g., 'XIZ'
        XIZ
        >>> 
        >>> # For reproducibility
        >>> ps = random_pauli_string(4, seed=42)
    """
    if seed is not None:
        random.seed(seed)
    
    gates = ['I', 'X', 'Y', 'Z']
    return ''.join(random.choice(gates) for _ in range(n_qubits))


