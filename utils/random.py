"""
psiqit/utils/random.py
============================================================
Random Quantum State and Operator Generation
============================================================

Tools for generating random quantum objects:
    • Random pure states (Haar measure)
    • Random density matrices
    • Random unitary operators
    • Random Hermitian operators
    • Random Pauli rotations

Random generation is essential for testing quantum algorithms, simulating
random circuits, and benchmarking quantum protocols. This module provides
functions to generate random quantum states and operators with various
probability distributions (Haar measure for unitaries and pure states).

Example:
    >>> from psiqit.utils.random import random_state, random_unitary, random_density_matrix
    >>> from psiqit.utils.validation import is_unitary, is_hermitian
    >>> 
    >>> # Random pure state
    >>> psi = random_state(2)
    >>> print(psi.is_normalized)
    True
    >>> 
    >>> # Random unitary matrix
    >>> U = random_unitary(2)
    >>> print(is_unitary(U).is_valid)
    True
    >>> 
    >>> # Random density matrix
    >>> rho = random_density_matrix(2)
    >>> from psiqit.utils.validation import is_density_matrix
    >>> print(is_density_matrix(rho).is_valid)
    True

References:
    F. Mezzadri, "How to generate random matrices from the classical compact groups,"
    Notices of the AMS, 54(5):592-604, 2007.
    I. Bengtsson and K. Życzkowski, "Geometry of Quantum States,"
    Cambridge University Press, 2006.
"""

