def random_density_matrix(dim: int, rank: Optional[int] = None, 
                          seed: Optional[int] = None) -> List[List[complex]]:
    """
    Generate a random density matrix.

    The density matrix is generated as ρ = AA† / Tr(AA†), where A is a
    dim × rank matrix with random complex Gaussian entries. This produces
    a random positive semidefinite matrix with the specified rank.

    Args:
        dim: Dimension of Hilbert space.
        rank: Rank of density matrix (if None, full rank = dim).
        seed: Random seed for reproducibility.

    Returns:
        Random density matrix (positive semidefinite, trace = 1).

    Raises:
        ValueError: If rank > dim.

    Examples:
        >>> # Full rank density matrix (maximally mixed on average)
        >>> rho = random_density_matrix(2)
        >>> 
        >>> # Rank-1 density matrix (pure state)
        >>> rho = random_density_matrix(2, rank=1)
        >>> from psiqit.utils.conversion import is_pure_state
        >>> print(is_pure_state(rho))
        True
        >>> 
        >>> # 3x3 density matrix with rank 2
        >>> rho = random_density_matrix(3, rank=2)
    """
    if seed is not None:
        random.seed(seed)
    
    if rank is None:
        rank = dim
    elif rank > dim:
        raise ValueError(f"Rank {rank} cannot exceed dimension {dim}")
    
    # Generate random matrix A (dim × rank)
    A = []
    for _ in range(dim):
        row = []
        for _ in range(rank):
            real = random.gauss(0, 1)
            imag = random.gauss(0, 1)
            row.append(complex(real, imag))
        A.append(row)
    
    # Make positive semidefinite: ρ = A A†
    rho = [[0j] * dim for _ in range(dim)]
    for i in range(dim):
        for j in range(dim):
            for k in range(rank):
                rho[i][j] += A[i][k] * A[j][k].conjugate()
    
    # Normalize to trace 1
    tr = trace(rho)
    tr_real = tr.real if hasattr(tr, 'real') else tr
    
    if tr_real > 0:
        for i in range(dim):
            for j in range(dim):
                rho[i][j] /= tr_real
    
    return rho


def random_qubit_density_matrix(seed: Optional[int] = None) -> List[List[complex]]:
    """
    Generate a random single-qubit density matrix.

    Args:
        seed: Random seed for reproducibility.

    Returns:
        Random 2x2 density matrix.

    Examples:
        >>> rho = random_qubit_density_matrix()
        >>> from psiqit.utils.validation import is_density_matrix
        >>> print(is_density_matrix(rho).is_valid)
        True
    """
    return random_density_matrix(2, seed=seed)


def _eigen(matrix):
    """
    Compute eigenvalues and eigenvectors (simplified for small matrices).

    This is a fallback when numpy is not available.
    """
    # For small matrices, use numpy if available
    try:
        import numpy as np
        M = np.array(matrix, dtype=complex)
        vals, vecs = np.linalg.eig(M)
        return vals.tolist(), vecs.T.tolist()
    except ImportError:
        # Fallback for 2x2 matrices
        if len(matrix) == 2:
            a, b = matrix[0][0], matrix[0][1]
            c, d = matrix[1][0], matrix[1][1]
            
            trace_val = a + d
            det_val = a * d - b * c
            
            disc = trace_val**2 - 4 * det_val
            sqrt_disc = math.sqrt(disc) if disc.real >= 0 else math.sqrt(disc)
            
            val1 = (trace_val + sqrt_disc) / 2
            val2 = (trace_val - sqrt_disc) / 2
            
            # Simple eigenvectors
            if abs(b) > 1e-10:
                vec1 = [b, val1 - a]
                vec2 = [b, val2 - a]
            else:
                vec1 = [1, 0]
                vec2 = [0, 1]
            
            # Normalize
            norm1 = math.sqrt(abs(vec1[0])**2 + abs(vec1[1])**2)
            norm2 = math.sqrt(abs(vec2[0])**2 + abs(vec2[1])**2)
            vec1 = [v / norm1 for v in vec1]
            vec2 = [v / norm2 for v in vec2]
            
            return [val1, val2], [vec1, vec2]
        else:
            raise NotImplementedError("Eigen decomposition for larger matrices requires numpy")


