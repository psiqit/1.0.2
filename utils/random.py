def random_state(dim: int, seed: Optional[int] = None) -> Ket:
    """
    Generate a random pure state according to the Haar measure.

    The state is generated by taking random complex Gaussian entries
    and normalizing. This produces a uniformly random state on the
    sphere in C^d.

    Args:
        dim: Dimension of Hilbert space.
        seed: Random seed for reproducibility.

    Returns:
        Random normalized pure state.

    Examples:
        >>> psi = random_state(2)
        >>> print(psi.is_normalized)
        True
        >>> 
        >>> # Reproducible random state
        >>> psi1 = random_state(2, seed=42)
        >>> psi2 = random_state(2, seed=42)
        >>> assert psi1.data == psi2.data
        >>> 
        >>> # 4-dimensional (2-qubit) random state
        >>> psi = random_state(4)
        >>> print(psi.dim)
        4
    """
    if seed is not None:
        random.seed(seed)
    
    # Generate random complex amplitudes
    amplitudes = []
    for _ in range(dim):
        real = random.gauss(0, 1)
        imag = random.gauss(0, 1)
        amplitudes.append(complex(real, imag))
    
    # Normalize
    norm = math.sqrt(sum(abs(a)**2 for a in amplitudes))
    if norm > 0:
        amplitudes = [a / norm for a in amplitudes]
    
    return Ket(amplitudes, _normalized=True)


def random_qubit_state(seed: Optional[int] = None) -> Ket:
    """
    Generate a random single-qubit state.

    Args:
        seed: Random seed for reproducibility.

    Returns:
        Random normalized qubit state.

    Examples:
        >>> psi = random_qubit_state()
        >>> print(psi.dim)
        2
        >>> 
        >>> # With seed for reproducibility
        >>> psi = random_qubit_state(seed=42)
    """
    return random_state(2, seed)


def random_n_qubit_state(n_qubits: int, seed: Optional[int] = None) -> Ket:
    """
    Generate a random n-qubit state.

    Args:
        n_qubits: Number of qubits.
        seed: Random seed for reproducibility.

    Returns:
        Random normalized n-qubit state.

    Examples:
        >>> psi = random_n_qubit_state(3)  # 3-qubit random state
        >>> print(psi.dim)  # 2^3 = 8
        8
    """
    dim = 2 ** n_qubits
    return random_state(dim, seed)


