@dataclass
class RandomResult:
    """
    Result container for random generation.

    Attributes:
        result: The generated random object.
        seed: Random seed used (if any).
        type: Type of object generated ('state', 'density_matrix', 'unitary', etc.).
        dimension: Dimension of the generated object.

    Examples:
        >>> result = RandomResult(result=psi, seed=42, type='state', dimension=2)
        >>> print(result)
        Random state (dim=2, seed=42)
    """
    result: any
    seed: Optional[int]
    type: str
    dimension: int
    
    def __str__(self) -> str:
        """
        Return a human-readable string representation.

        Returns:
            String showing type, dimension, and seed.
        """
        return f"Random {self.type} (dim={self.dimension}, seed={self.seed})"


